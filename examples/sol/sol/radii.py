def smd_oxygen_rad(solvent):
    if solvent in hbonda.hbonda:
        alpha = hbonda.hbonda[solvent]
    else:
        alpha = 0.0
    if alpha>0.429:
        return 1.52
    else:    
        return 1.52 + 1.8*(0.43-alpha)


